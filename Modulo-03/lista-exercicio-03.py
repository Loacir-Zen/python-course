estado_civil = input('Entre com valor C-Casado, S-Solteiro, D-Divorciado, V-Víuvo, O-Outros: ')
if estado_civil == 'C':
    print('C-Casado')
elif estado_civil == 'S':
    print('S-Solteiro')
elif estado_civil == 'D':
    print('D-Divorciado')
elif estado_civil == 'V':
    print('V-Víuvo')
else: 
    print('O-Outros')