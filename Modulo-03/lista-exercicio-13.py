dia_semana = int(input('Entre com o dia da semana: '))

if dia_semana == 1:
    print('Domingo')
elif dia_semana == 2:
    print('Segunda-feira')
elif dia_semana == 3:
    print('terça-feira')
elif dia_semana == 4:
    print('quarta-feira')
elif dia_semana == 5:
    print('quinta-feira')
elif dia_semana == 6:
    print('sexta-feira')
elif dia_semana == 7:
    print('sabado')
else:
    print('valor inválido')